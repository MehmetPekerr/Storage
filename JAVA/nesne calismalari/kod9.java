
public class kod9 {

	public static void main(String[] args) {
		
		
		System.out.println(Math.ceil(1.1)); //bir üste yuvarlar 2 olur
		System.out.println(Math.floor(1.1));//bi alta yuvarlar 1 olur
		System.out.println(Math.round(1.5));//en yakına yuvarlar(burada 2 ye yuvarlar
		System.out.println(Math.cos(30));   //sin de olur
		System.out.println(Math.max(7, 8)); //min de olur
		System.out.println(Math.pow(2, 3)); //2 üssü 3 = 8
		System.out.println(Math.abs(-6));   //mutlak değerini alır
		System.out.println(Math.sqrt(16));  //karekökü 4 tür
        System.out.println(Math.random());  //0 ile 1 arasında rastgele sayı atar
		
		

	}

}
